#include <stdio.h>
int main()
{
	int a,b,add,sub,div,mul,modulus;
	scanf("%d%d",&a,&b);
	add=a+b;
	sub=a-b;
	div=a/b;
	mul=a*b;
	modulus=a%b;
	printf("addition is %d:\n",add);
	printf("subraction is %d:\n",sub);
	printf("division is %d:\n",div);
	printf("multiplication is %d:\n",mul);
	printf("the remainder is %d:",modulus);
	


}
